public class PlanEntrenamiento {
    
    private int id_PlanE;
    private String NombreP;
    private float ValorP;
    private int DuracionP;

    public int getId_PlanE() {
        return id_PlanE;
    }

    public void setId_PlanE(int id_PlanE) {
        this.id_PlanE = id_PlanE;
    }

    public String getNombreP() {
        return NombreP;
    }

    public void setNombreP(String NombreP) {
        this.NombreP = NombreP;
    }

    public float getValorP() {
        return ValorP;
    }

    public void setValorP(float ValorP) {
        this.ValorP = ValorP;
    }

    public int getDuracionP() {
        return DuracionP;
    }

    public void setDuracionP(int DuracionP) {
        this.DuracionP = DuracionP;
    }
    
    
    
}
