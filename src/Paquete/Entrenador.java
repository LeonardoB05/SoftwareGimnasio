
package Paquete;

public class Entrenador {
    
    private int id_entrenador;
    private String NombreEnt;
    private int TelefonoEnt;
    private String EspecialidadEnt;

    public int getId_entrenador() {
        return id_entrenador;
    }

    public void setId_entrenador(int id_entrenador) {
        this.id_entrenador = id_entrenador;
    }

    public String getNombreEnt() {
        return NombreEnt;
    }

    public void setNombreEnt(String NombreEnt) {
        this.NombreEnt = NombreEnt;
    }

    public int getTelefonoEnt() {
        return TelefonoEnt;
    }

    public void setTelefonoEnt(int TelefonoEnt) {
        this.TelefonoEnt = TelefonoEnt;
    }

    public String getEspecialidadEnt() {
        return EspecialidadEnt;
    }

    public void setEspecialidadEnt(String EspecialidadEnt) {
        this.EspecialidadEnt = EspecialidadEnt;
    }
    
    
    
}
