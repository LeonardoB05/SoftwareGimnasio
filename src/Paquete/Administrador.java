public class Administrador {
    
    private int id_Admin;
    private String ContraseñaAdmin;
    private String NombreAdmin;
    private int TelefonoAdmin;

    public int getId_Admin() {
        return id_Admin;
    }

    public void setId_Admin(int id_Admin) {
        this.id_Admin = id_Admin;
    }

    public String getContraseñaAdmin() {
        return ContraseñaAdmin;
    }

    public void setContraseñaAdmin(String ContraseñaAdmin) {
        this.ContraseñaAdmin = ContraseñaAdmin;
    }

    public String getNombreAdmin() {
        return NombreAdmin;
    }

    public void setNombreAdmin(String NombreAdmin) {
        this.NombreAdmin = NombreAdmin;
    }

    public int getTelefonoAdmin() {
        return TelefonoAdmin;
    }

    public void setTelefonoAdmin(int TelefonoAdmin) {
        this.TelefonoAdmin = TelefonoAdmin;
    }
    
    
    
}
